package model;

public class Relatorio {

	private String nomeRota;
	private String nomeMotorista;
	private String horaRelatorio;
	private String dataRelatorio;
	private String nomeEmpresa;
	private String nomePassageiro;
	private String cpfPassageiro;
	private String telefonePassageiro;

	public String getNomeRota() {
		return nomeRota;
	}

	public void setNomeRota(String nomeRota) {
		this.nomeRota = nomeRota;
	}

	public String getNomeMotorista() {
		return nomeMotorista;
	}

	public void setNomeMotorista(String nomeMotorista) {
		this.nomeMotorista = nomeMotorista;
	}

	public String getHoraRelatorio() {
		return horaRelatorio;
	}

	public void setHoraRelatorio(String horaRelatorio) {
		this.horaRelatorio = horaRelatorio;
	}

	public String getDataRelatorio() {
		return dataRelatorio;
	}

	public void setDataRelatorio(String data) {
		this.dataRelatorio = data;
	}

	public String getNomeEmpresa() {
		return nomeEmpresa;
	}

	public void setNomeEmpresa(String nomeEmpresa) {
		this.nomeEmpresa = nomeEmpresa;
	}

	public String getNomePassageiro() {
		return nomePassageiro;
	}

	public void setNomePassageiro(String nomePassageiro) {
		this.nomePassageiro = nomePassageiro;
	}

	public String getCpfPassageiro() {
		return cpfPassageiro;
	}

	public void setCpfPassageiro(String cpfPassageiro) {
		this.cpfPassageiro = cpfPassageiro;
	}

	public String getTelefonePassageiro() {
		return telefonePassageiro;
	}

	public void setTelefonePassageiro(String telefonePassageiro) {
		this.telefonePassageiro = telefonePassageiro;
	}

}
